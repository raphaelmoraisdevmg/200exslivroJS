/* Declare duas variáveis e atribua valores  booleanos a elas. 
Use os operadores lógicos AND, OR e Not para realizar operações lógicas entre as variáveis. imprima os resultados
 */

let proposicaoA = false;
let proposicaoB=  true;

console.log(proposicaoA && proposicaoB) //false
console.log(proposicaoA || proposicaoB) //true
console.log(proposicaoA || !proposicaoB) // false;  // ! é o operador de negação 
console.log(!proposicaoA && proposicaoB) // true;